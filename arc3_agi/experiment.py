"""Experiment tracking store backed by PostgreSQL.

Provides :class:`ExperimentStore` for persisting per-generation fitness
statistics from parallel population runs and associating them with named,
described experiments.  Experiment names are claimed atomically before a run
starts so multiple machines can safely share one database.

Typical workflow::

    from arc3_agi.experiment import ExperimentStore

    store = ExperimentStore()  # uses DATABASE_URL or the local development URL
    claim = store.claim_experiment(
        name="baseline",
        description="100 pops, default params",
        run_id="20260717T210555_6ae7c2",
        params={"max_generations": 1000, "population_size": 100},
    )
    store.mark_experiment_running(claim.experiment_id)
    store.ingest_run(claim.experiment_id, Path("runs/20260717T210555_6ae7c2"))
    store.mark_experiment_completed(claim.experiment_id)
    df = store.load_stats(claim.experiment_id)   # pandas DataFrame
"""

from __future__ import annotations

import json
import os
import socket
from dataclasses import dataclass
from datetime import datetime, timezone
from pathlib import Path
from typing import Any

import pandas as pd
import psycopg
from psycopg.sql import SQL
from psycopg.types.json import Jsonb

DEFAULT_DATABASE_URL = "postgresql://arc3_agi:arc3_agi@localhost:5432/arc3_agi"
"""Default local PostgreSQL connection URL used when ``DATABASE_URL`` is unset."""

_SCHEMA_SQL = """
CREATE TABLE IF NOT EXISTS experiments (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         TEXT        NOT NULL UNIQUE,
    description  TEXT,
    run_id       TEXT,
    status       TEXT        NOT NULL DEFAULT 'completed'
        CHECK (status IN ('claimed', 'running', 'completed', 'failed')),
    claimed_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    started_at   TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    failed_at    TIMESTAMPTZ,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    host         TEXT,
    process_id   INTEGER,
    error        TEXT,
    params_json  JSONB       NOT NULL DEFAULT '{}'::jsonb
);

ALTER TABLE experiments ADD COLUMN IF NOT EXISTS status TEXT NOT NULL DEFAULT 'completed';
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS claimed_at TIMESTAMPTZ NOT NULL DEFAULT now();
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS started_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS completed_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS failed_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS host TEXT;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS process_id INTEGER;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS error TEXT;
CREATE UNIQUE INDEX IF NOT EXISTS experiments_name_key ON experiments (name);

CREATE TABLE IF NOT EXISTS generation_stats (
    experiment_id BIGINT  NOT NULL,
    pop_id        INTEGER NOT NULL,
    generation    INTEGER NOT NULL,
    min_fitness   REAL    NOT NULL,
    max_fitness   REAL    NOT NULL,
    mean_fitness  REAL    NOT NULL,
    duration_s    REAL    NOT NULL,
    PRIMARY KEY (experiment_id, pop_id, generation),
    FOREIGN KEY (experiment_id) REFERENCES experiments(id) ON DELETE CASCADE
);
"""


@dataclass(frozen=True)
class ExperimentClaim:
    """Result returned by :meth:`ExperimentStore.claim_experiment`."""

    experiment_id: int
    status: str
    already_completed: bool = False


class ExperimentClaimError(RuntimeError):
    """Raised when an experiment name is already claimed by another run."""


def resolve_database_url(database_url: str | None = None) -> str:
    """Return the PostgreSQL URL to use for experiment tracking."""
    return database_url or os.environ.get("DATABASE_URL") or DEFAULT_DATABASE_URL


class ExperimentStore:
    """PostgreSQL-backed store for experiment metadata and generation stats.

    Parameters
    ----------
    database_url:
        PostgreSQL connection URL.  When omitted, :envvar:`DATABASE_URL` is
        used, falling back to :data:`DEFAULT_DATABASE_URL` for local
        development.
    """

    def __init__(self, database_url: str | None = None) -> None:
        self.database_url = resolve_database_url(database_url)
        _conn = psycopg.connect(self.database_url, autocommit=True)
        try:
            _conn.execute(_SCHEMA_SQL)
        except Exception:
            _conn.close()
            raise
        self._conn = _conn

    # ------------------------------------------------------------------
    # Public API
    # ------------------------------------------------------------------

    def create_experiment(
        self,
        name: str,
        description: str = "",
        run_id: str = "",
        params: dict[str, Any] | None = None,
    ) -> int:
        """Insert a new experiment record and return its id.

        Parameters
        ----------
        name:
            Short human-readable name (e.g. ``"baseline"``).
        description:
            Free-text description of the experiment's purpose and settings.
        run_id:
            The run identifier (matches the ``runs/<run_id>/`` directory).
        params:
            Dictionary of configuration parameters to store as JSON.

        Returns
        -------
        int
            The auto-assigned experiment id.
        """
        created_at = datetime.now(tz=timezone.utc)
        with self._conn.transaction():
            row = self._conn.execute(
                """
                INSERT INTO experiments
                    (name, description, run_id, status, claimed_at, completed_at,
                     created_at, host, process_id, params_json)
                VALUES (%s, %s, %s, 'completed', %s, %s, %s, %s, %s, %s)
                RETURNING id
                """,
                [
                    name,
                    description,
                    run_id,
                    created_at,
                    created_at,
                    created_at,
                    socket.gethostname(),
                    os.getpid(),
                    Jsonb(params or {}),
                ],
            ).fetchone()
        return int(row[0])  # type: ignore[index]

    def claim_experiment(
        self,
        name: str,
        description: str = "",
        run_id: str = "",
        params: dict[str, Any] | None = None,
    ) -> ExperimentClaim:
        """Atomically claim *name* before running an experiment.

        A newly inserted row is returned with ``status == "claimed"``.  If the
        name already belongs to a completed experiment, the existing id is
        returned with ``already_completed`` set.  Any non-completed existing row
        raises :class:`ExperimentClaimError` so concurrent runners do not both
        execute the same experiment name.
        """
        claimed_at = datetime.now(tz=timezone.utc)
        with self._conn.transaction():
            row = self._conn.execute(
                """
                INSERT INTO experiments
                    (name, description, run_id, status, claimed_at, created_at,
                     host, process_id, params_json)
                VALUES (%s, %s, %s, 'claimed', %s, %s, %s, %s, %s)
                ON CONFLICT (name) DO NOTHING
                RETURNING id
                """,
                [
                    name,
                    description,
                    run_id,
                    claimed_at,
                    claimed_at,
                    socket.gethostname(),
                    os.getpid(),
                    Jsonb(params or {}),
                ],
            ).fetchone()
            if row is not None:
                return ExperimentClaim(experiment_id=int(row[0]), status="claimed")

            existing = self._conn.execute(
                """
                SELECT id, status
                FROM experiments
                WHERE name = %s
                """,
                [name],
            ).fetchone()

        if existing is None:
            raise ExperimentClaimError(
                f"Experiment '{name}' could not be claimed and no existing row was found."
            )

        experiment_id = int(existing[0])
        status = str(existing[1])
        if status == "completed":
            return ExperimentClaim(
                experiment_id=experiment_id,
                status="completed",
                already_completed=True,
            )
        raise ExperimentClaimError(
            f"Experiment '{name}' already exists with status '{status}' (id={experiment_id})."
        )

    def get_experiment_id_by_name(self, name: str) -> int | None:
        """Return the id for an existing experiment named *name*, if any."""
        row = self._conn.execute(
            """
            SELECT id
            FROM experiments
            WHERE name = %s
            ORDER BY id
            LIMIT 1
            """,
            [name],
        ).fetchone()
        return int(row[0]) if row is not None else None

    def mark_experiment_running(self, experiment_id: int) -> None:
        """Mark a claimed experiment as actively running."""
        self._conn.execute(
            """
            UPDATE experiments
            SET status = 'running', started_at = COALESCE(started_at, %s), error = NULL
            WHERE id = %s
            """,
            [datetime.now(tz=timezone.utc), experiment_id],
        )

    def mark_experiment_completed(self, experiment_id: int) -> None:
        """Mark an experiment as completed."""
        self._conn.execute(
            """
            UPDATE experiments
            SET status = 'completed', completed_at = %s, failed_at = NULL, error = NULL
            WHERE id = %s
            """,
            [datetime.now(tz=timezone.utc), experiment_id],
        )

    def mark_experiment_failed(self, experiment_id: int, error: str) -> None:
        """Mark an experiment as failed and store a diagnostic message."""
        self._conn.execute(
            """
            UPDATE experiments
            SET status = 'failed', failed_at = %s, error = %s
            WHERE id = %s
            """,
            [datetime.now(tz=timezone.utc), error, experiment_id],
        )

    def ingest_run(self, experiment_id: int, run_dir: Path | str) -> int:
        """Read all ``fitness_history.json`` files under *run_dir* and insert
        their generation stats into the database.

        Each population subprocess writes a ``fitness_history.json`` directly
        inside its ``pop_N/`` directory (not inside the timestamped
        subdirectory).  This method globs for those files under *run_dir*.

        Parameters
        ----------
        experiment_id:
            Id returned by :meth:`create_experiment`.
        run_dir:
            Path to the run directory, e.g. ``runs/20260717T210555_6ae7c2``.

        Returns
        -------
        int
            Total number of generation-stat rows inserted.
        """
        run_dir = Path(run_dir)
        rows: list[tuple[int, int, int, float, float, float, float]] = []

        for history_file in sorted(run_dir.glob("pop_*/fitness_history.json")):
            with history_file.open() as fh:
                data = json.load(fh)
            pop_id: int = data["pop_id"]
            for entry in data["history"]:
                rows.append(
                    (
                        experiment_id,
                        pop_id,
                        entry["generation"],
                        entry["min_fitness"],
                        entry["max_fitness"],
                        entry["mean_fitness"],
                        entry["duration_s"],
                    )
                )

        if rows:
            with self._conn.transaction():
                with self._conn.cursor() as cursor:
                    cursor.executemany(
                        """
                        INSERT INTO generation_stats
                            (experiment_id, pop_id, generation,
                             min_fitness, max_fitness, mean_fitness, duration_s)
                        VALUES (%s, %s, %s, %s, %s, %s, %s)
                        ON CONFLICT (experiment_id, pop_id, generation) DO UPDATE SET
                            min_fitness = EXCLUDED.min_fitness,
                            max_fitness = EXCLUDED.max_fitness,
                            mean_fitness = EXCLUDED.mean_fitness,
                            duration_s = EXCLUDED.duration_s
                        """,
                        rows,
                    )
        return len(rows)

    def list_experiments(self) -> pd.DataFrame:
        """Return a DataFrame of all experiments with summary counts.

        Columns: ``id``, ``name``, ``description``, ``run_id``, ``created_at``,
        ``pop_count``, ``gen_count``, ``params_json``.
        """
        return self._read_dataframe(SQL("""
            SELECT
                e.id,
                e.name,
                e.description,
                e.run_id,
                e.created_at,
                e.status,
                COUNT(DISTINCT gs.pop_id)    AS pop_count,
                MAX(gs.generation)           AS gen_count,
                e.params_json::text          AS params_json
            FROM experiments e
            LEFT JOIN generation_stats gs ON gs.experiment_id = e.id
            GROUP BY e.id, e.name, e.description, e.run_id, e.created_at, e.status, e.params_json
            ORDER BY e.id
            """))

    def load_stats(self, experiment_id: int) -> pd.DataFrame:
        """Return all generation-level stats for *experiment_id*.

        Columns: ``pop_id``, ``generation``, ``min_fitness``, ``max_fitness``,
        ``mean_fitness``, ``duration_s``.
        """
        return self._read_dataframe(
            SQL("""
            SELECT pop_id, generation, min_fitness, max_fitness, mean_fitness, duration_s
            FROM generation_stats
            WHERE experiment_id = %s
            ORDER BY pop_id, generation
            """),
            [experiment_id],
        )

    def delete_experiment(self, experiment_id: int) -> None:
        """Remove an experiment and all its generation stats.

        Parameters
        ----------
        experiment_id:
            Id of the experiment to delete.
        """
        self._conn.execute(
            "DELETE FROM generation_stats WHERE experiment_id = %s", [experiment_id]
        )
        self._conn.execute("DELETE FROM experiments WHERE id = %s", [experiment_id])

    def _read_dataframe(
        self, query: SQL, params: list[Any] | tuple[Any, ...] | None = None
    ) -> pd.DataFrame:
        """Execute *query* and return the result as a pandas DataFrame."""
        with self._conn.cursor() as cursor:
            cursor.execute(query, params or [])
            rows = cursor.fetchall()
            columns = [
                column.name if hasattr(column, "name") else column[0]
                for column in cursor.description or []
            ]
        return pd.DataFrame(rows, columns=columns)

    def close(self) -> None:
        """Close the underlying PostgreSQL connection."""
        if self._conn is not None and not self._conn.closed:
            self._conn.close()

    def __enter__(self) -> "ExperimentStore":
        return self

    def __exit__(self, *_: Any) -> None:
        self.close()
