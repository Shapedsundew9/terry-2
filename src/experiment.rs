/// PostgreSQL-backed experiment tracking store.
///
/// Mirrors Python's `arc3_agi.experiment.ExperimentStore` — same schema,
/// same lifecycle (claim → running → completed / failed), same
/// `ingest_run` logic that reads `pop_*/fitness_history.json` files and
/// bulk-upserts into `generation_stats`.
use std::path::Path;

use postgres::{Client, NoTls};
use serde_json::Value;

// Identical to Python's _SCHEMA_SQL (idempotent — safe to run on every start).
const SCHEMA_SQL: &str = "
CREATE TABLE IF NOT EXISTS experiments (
    id           BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name         TEXT        NOT NULL UNIQUE,
    description  TEXT,
    run_id       TEXT,
    status       TEXT        NOT NULL DEFAULT 'completed'
        CHECK (status IN ('claimed', 'running', 'completed', 'failed')),
    claimed_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    started_at   TIMESTAMPTZ,
    completed_at TIMESTAMPTZ,
    failed_at    TIMESTAMPTZ,
    created_at   TIMESTAMPTZ NOT NULL DEFAULT now(),
    host         TEXT,
    process_id   INTEGER,
    error        TEXT,
    params_json  JSONB       NOT NULL DEFAULT '{}'::jsonb,
    pop_count    INTEGER,
    gen_count    INTEGER
);

ALTER TABLE experiments ADD COLUMN IF NOT EXISTS status TEXT NOT NULL DEFAULT 'completed';
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS claimed_at TIMESTAMPTZ NOT NULL DEFAULT now();
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS started_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS completed_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS failed_at TIMESTAMPTZ;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS host TEXT;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS process_id INTEGER;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS error TEXT;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS pop_count INTEGER;
ALTER TABLE experiments ADD COLUMN IF NOT EXISTS gen_count INTEGER;
CREATE UNIQUE INDEX IF NOT EXISTS experiments_name_key ON experiments (name);

CREATE TABLE IF NOT EXISTS generation_stats (
    experiment_id BIGINT  NOT NULL,
    pop_id        INTEGER NOT NULL,
    generation    INTEGER NOT NULL,
    min_fitness   REAL    NOT NULL,
    max_fitness   REAL    NOT NULL,
    mean_fitness  REAL    NOT NULL,
    duration_s    REAL    NOT NULL,
    PRIMARY KEY (experiment_id, pop_id, generation),
    FOREIGN KEY (experiment_id) REFERENCES experiments(id) ON DELETE CASCADE
);
";

const DEFAULT_DATABASE_URL: &str = "postgresql://arc3_agi:arc3_agi@localhost:5432/arc3_agi";

pub fn resolve_database_url(explicit: Option<&str>) -> String {
    if let Some(url) = explicit {
        return url.to_string();
    }
    if let Ok(url) = std::env::var("DATABASE_URL") {
        return url;
    }
    DEFAULT_DATABASE_URL.to_string()
}

// ---------------------------------------------------------------------------
// ExperimentStore
// ---------------------------------------------------------------------------

pub struct ExperimentStore {
    client: Client,
}

impl ExperimentStore {
    pub fn connect(database_url: &str) -> Result<Self, postgres::Error> {
        let mut client = Client::connect(database_url, NoTls)?;
        client.batch_execute(SCHEMA_SQL)?;
        Ok(ExperimentStore { client })
    }

    /// Claim an experiment name atomically.
    ///
    /// Returns `(experiment_id, already_completed)`.
    /// - `already_completed = true` → run is skipped, use existing id.
    /// - Raises an error if the name is claimed/running by another process.
    pub fn claim_experiment(
        &mut self,
        name: &str,
        description: &str,
        run_id: &str,
        params_json: &Value,
    ) -> Result<(i64, bool), Box<dyn std::error::Error>> {
        let host = hostname::get()
            .map(|h| h.to_string_lossy().into_owned())
            .unwrap_or_else(|_| "unknown".into());
        let pid = std::process::id() as i32;

        // Atomic INSERT … ON CONFLICT DO NOTHING.
        let row = self.client.query_opt(
            "INSERT INTO experiments (name, description, run_id, status, host, process_id, params_json)
             VALUES ($1, $2, $3, 'claimed', $4, $5, $6)
             ON CONFLICT (name) DO NOTHING
             RETURNING id",
            &[&name, &description, &run_id, &host, &pid, params_json],
        )?;

        if let Some(row) = row {
            let id: i64 = row.get(0);
            return Ok((id, false));
        }

        // Conflict — check existing status.
        let existing = self.client.query_one(
            "SELECT id, status FROM experiments WHERE name = $1",
            &[&name],
        )?;
        let id: i64 = existing.get(0);
        let status: &str = existing.get(1);

        if status == "completed" {
            return Ok((id, true));
        }

        Err(format!(
            "Experiment '{name}' already exists with status '{status}' — \
             cannot claim"
        )
        .into())
    }

    pub fn mark_running(&mut self, experiment_id: i64) -> Result<(), postgres::Error> {
        self.client.execute(
            "UPDATE experiments
             SET status = 'running',
                 started_at = COALESCE(started_at, now()),
                 error = NULL
             WHERE id = $1",
            &[&experiment_id],
        )?;
        Ok(())
    }

    pub fn mark_completed(&mut self, experiment_id: i64) -> Result<(), postgres::Error> {
        self.client.execute(
            "UPDATE experiments
             SET status = 'completed',
                 completed_at = now(),
                 failed_at = NULL,
                 error = NULL
             WHERE id = $1",
            &[&experiment_id],
        )?;
        Ok(())
    }

    pub fn mark_failed(&mut self, experiment_id: i64, error: &str) -> Result<(), postgres::Error> {
        self.client.execute(
            "UPDATE experiments
             SET status = 'failed',
                 failed_at = now(),
                 error = $2
             WHERE id = $1",
            &[&experiment_id, &error],
        )?;
        Ok(())
    }

    /// Read all `pop_*/fitness_history.json` files under `run_dir` and
    /// upsert their records into `generation_stats`.
    ///
    /// Returns the number of rows upserted.
    pub fn ingest_run(
        &mut self,
        experiment_id: i64,
        run_dir: &Path,
    ) -> Result<usize, Box<dyn std::error::Error>> {
        let mut total = 0usize;

        // Collect pop_*/fitness_history.json paths.
        let mut fh_paths: Vec<std::path::PathBuf> = std::fs::read_dir(run_dir)?
            .filter_map(|e| e.ok())
            .filter(|e| e.file_name().to_string_lossy().starts_with("pop_"))
            .map(|e| e.path().join("fitness_history.json"))
            .filter(|p| p.exists())
            .collect();
        fh_paths.sort();

        for entry in fh_paths {
            let json_str = std::fs::read_to_string(&entry)?;
            let doc: Value = serde_json::from_str(&json_str)?;

            let pop_id = doc["pop_id"].as_i64().unwrap_or(0) as i32;
            let history = doc["history"].as_array().ok_or("missing history array")?;

            for record in history {
                let generation = record["generation"].as_i64().unwrap_or(0) as i32;
                let min_f = record["min_fitness"].as_f64().unwrap_or(0.0) as f32;
                let max_f = record["max_fitness"].as_f64().unwrap_or(0.0) as f32;
                let mean_f = record["mean_fitness"].as_f64().unwrap_or(0.0) as f32;
                let dur = record["duration_s"].as_f64().unwrap_or(0.0) as f32;

                self.client.execute(
                    "INSERT INTO generation_stats
                        (experiment_id, pop_id, generation,
                         min_fitness, max_fitness, mean_fitness, duration_s)
                     VALUES ($1, $2, $3, $4, $5, $6, $7)
                     ON CONFLICT (experiment_id, pop_id, generation)
                     DO UPDATE SET
                        min_fitness  = EXCLUDED.min_fitness,
                        max_fitness  = EXCLUDED.max_fitness,
                        mean_fitness = EXCLUDED.mean_fitness,
                        duration_s   = EXCLUDED.duration_s",
                    &[
                        &experiment_id,
                        &pop_id,
                        &generation,
                        &min_f,
                        &max_f,
                        &mean_f,
                        &dur,
                    ],
                )?;
                total += 1;
            }
        }

        // Update pop_count / gen_count summary on experiments row.
        self.client.execute(
            "UPDATE experiments
             SET pop_count = (
                     SELECT COUNT(DISTINCT pop_id)
                     FROM generation_stats
                     WHERE experiment_id = $1
                 ),
                 gen_count = (
                     SELECT MAX(generation)
                     FROM generation_stats
                     WHERE experiment_id = $1
                 )
             WHERE id = $1",
            &[&experiment_id],
        )?;

        Ok(total)
    }
}
